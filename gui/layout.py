from PyQt5.QtWidgets import (
    QWidget, QVBoxLayout, QHBoxLayout, QLabel, QPushButton,
    QComboBox, QTextEdit, QFileDialog, QLineEdit, QSpinBox,
    QScrollArea, QFrame, QGridLayout
)
from PyQt5.QtCore import Qt

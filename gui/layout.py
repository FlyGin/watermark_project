    # –≠—Ç–∞ –∫–Ω–æ–ø–∫–∞ –∏–∑–≤–ª–µ–∫–∞–µ—Ç —Å–µ–∫—Ä–µ—Ç –∏–∑ —Å—Ç–µ–≥–æ-–∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
    
    # –ö–Ω–æ–ø–∫–∞ —Å–±—Ä–æ—Å–∞ –≤—Å–µ—Ö –Ω–∞—Å—Ç—Ä–æ–µ–∫

    lineedit_wm.setPlaceholderText("–í–≤–µ–¥–∏—Ç–µ —Ç–µ–∫—Å—Ç –¥–ª—è –≤—Å—Ç—Ä–∞–∏–≤–∞–Ω–∏—è...")
    wm_upload_layout.addWidget(lineedit_wm)

    upload_preview_layout_grid.addLayout(wm_upload_layout, 0, 1)

    # –ü—Ä–µ–¥–ø—Ä–æ—Å–º–æ—Ç—Ä –≤–æ–¥—è–Ω–æ–≥–æ –∑–Ω–∞–∫–∞

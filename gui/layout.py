    wm_upload_layout.addWidget(btn_load_wm)

    # –ü–æ–ª–µ –¥–ª—è –≤–≤–æ–¥–∞ —Ç–µ–∫—Å—Ç–∞ (–∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–∞ –∑–∞–≥—Ä—É–∑–∫–µ —Ñ–∞–π–ª–∞)

    # –°–ï–ö–¶–ò–Ø 6: –û–ë–õ–ê–°–¢–¨ –í–´–í–û–î–ê –†–ï–ó–£–õ–¨–¢–ê–¢–û–í
    # ========================================================================
    # –¢–µ–∫—Å—Ç–æ–≤–∞—è –æ–±–ª–∞—Å—Ç—å –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Å–æ–æ–±—â–µ–Ω–∏–π –æ —Ö–æ–¥–µ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –æ–ø–µ—Ä–∞—Ü–∏–π
    
